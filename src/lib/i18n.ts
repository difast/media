export type TranslationKey = keyof (typeof dictionaries)["ru"];

export function getDictionary(locale: Locale) {
  return dictionaries[locale] ?? dictionaries[SITE.defaultLocale];
}

export function isLocale(value: string): value is Locale {
  return (SITE.locales as readonly string[]).includes(value);
}

export function t(locale: Locale, key: TranslationKey): string {
  return getDictionary(locale)[key] ?? dictionaries.ru[key] ?? key;
}
